from hydra import initialize, compose

with initialize('../../configs', version_base='1.3'):
    cfg = compose('eval_config', overrides=['dataset=imagenet'])

model, tokenizer = CLIP(), CLIP_tokenizer()
dataset = create_dataset(cfg.dataset)

device = torch.device('cuda:0')
text_classifier = list()
model.eval()
model.to(device)

for class_name in tqdm(dataset.class_name, desc='Build Text Classifier'):
    class_name = class_name.replace('_', '')
    text = [t.format(class_name) for t in template]
    text_input = tokenizer(text).to(device)

    text_feature = model.encode_text(text_input)
    text_classifier.append(text_feature.detach().cpu())
    # text_feature /= text_feature.norm(dim=-1, keepdim=True).mean(dim=0)
    # text_feature /= text_feature.norm()

text_classifier = torch.stack(text_classifier, dim=0)
